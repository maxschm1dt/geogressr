# Function to convert country names to ISO Alpha-2 codes
def country_to_alpha2(country_name):
    try:
        country = pycountry.countries.lookup(country_name)
        return country.alpha_2
    except LookupError:
        return None  # Return None if the country name is not found

# Create a new dictionary with ISO Alpha-2 country codes
updated_data = {}

for country, coverage in data.items():
    country_code = country_to_alpha2(country)
    if country_code:  # Only add if the country code is found
        updated_data[country_code] = "L" if coverage == "Left Hand Traffic" else "R"
    else:
        print(f"Warning: Could not find country code for {country}")

# Save the updated data to a new JSON file
